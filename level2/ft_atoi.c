#include <stdio.h>
#include <stdlib.h>

int	ft_atoi(const char *str)
{
	int i = 0;
	int num = 0;
	int sign = 1;

	while (str[i] == '\t' || str[i] == '\0' || str[i] == ' ') 
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
			num = num * 10 + str[i++] - '0';
	return (num * sign);
}

